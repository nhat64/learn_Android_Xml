package com.example.retrofitexample.model;

public class Rate {
    private float rate;
    private  int count;

    public Rate() {
    }

    public float getRate() {
        return rate;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
